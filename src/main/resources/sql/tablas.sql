CREATE TABLE TIPO_TARJETA(
    ID SERIAL PRIMARY KEY,
    TIPO VARCHAR(25)
);

CREATE TABLE HORARIO(
    ID SERIAL PRIMARY KEY,
    FECHA DATE,
    HORA_INICIO TIMESTAMP,
    HORA_FIN TIMESTAMP,
    COSTO NUMERIC(5,2),
    ID_ASESOR INTEGER,
    ID_CURSO INTEGER,
    LINK_SALA TEXT,
    FOREIGN KEY (ID_ASESOR) REFERENCES ASESOR (ID),
    FOREIGN KEY (ID_CURSO) REFERENCES CURSO (ID)
);

CREATE TABLE PAGO_TARJETA(
