CREATE TABLE CURSO
(
    ID SERIAL PRIMARY KEY,
    NOMBRE VARCHAR(64) ,
    ID_AREA_ACADEMICA INTEGER,
    FOREIGN KEY(ID_AREA_ACADEMICA) REFERENCES AREA_ACADEMICA(ID)
);

CREATE TABLE RECOMENDACION
(
    ID SERIAL PRIMARY KEY,
    ID_ASESORIA INTEGER,
    COMENTARIO TEXT,
    FOREIGN KEY(ID_ASESORIA) REFERENCES ASESORIA(ID)
);
CREATE TABLE UNIVERSIDAD
(
    ID SERIAL PRIMARY KEY,
    NOMBRE VARCHAR(80)
);
CREATE TABLE USUARIO
(
    ID SERIAL PRIMARY KEY,

    NOMBRE VARCHAR(64),
    APELLIDO VARCHAR(64),
    DNI INTEGER,
    TELEFONO INTEGER,
    ID_UNIVERSIDAD INTEGER,
    EMAIL VARCHAR(64),
    CONTRASENIA VARCHAR(64),
    URL_FOTO VARCHAR(64),
    USERNAME VARCHAR(50),
    FOREIGN KEY(ID_UNIVERSIDAD) REFERENCES UNIVERSIDAD(ID)
);

CREATE TABLE AREA_ACADEMICA(
    ID SERIAL PRIMARY KEY,
    NOMBRE VARCHAR(50),
    ID_UNIVERSIDAD INTEGER,
    FOREIGN KEY (ID_UNIVERSIDAD) REFERENCES UNIVERSIDAD (ID)
);

CREATE TABLE ASESOR(
    ID SERIAL PRIMARY KEY,
    NOMBRE VARCHAR(12),
    APELLIDO VARCHAR(50),
    TELEFONO INTEGER,
    CORREO VARCHAR(30),
    CALIFICACION INTEGER
